/**
     * Hanterar logiken för att lägga till en ny kontakt i databasen.
     */
    // 1. Hämta text från inmatningsfälten
    // 2. Töm fälten så de är redo för nästa inmatning
    // 3. Validera att inga fält är tomma innan vi skickar till databasen
    // 4. Uppdatera listan i fönstret så den nya kontakten syns direkt
    private void BtnLäggtillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnLäggtillActionPerformed
        String Förnamn = this.tbxFörnamn.getText();
        this.tbxFörnamn.setText("");
        String Efternamn = this.tbxEfternamn.getText();
        this.tbxEfternamn.setText("");
        String Telefonnummer = this.tbxTelefonummer.getText();
        this.tbxTelefonummer.setText("");

        System.out.println(Telefonnummer);
        if (!Förnamn.isBlank() && !Efternamn.isBlank() && !Telefonnummer.isBlank()) {
            Kontakt kon = new Kontakt(Förnamn, Efternamn, Telefonnummer);
            db.insert(kon);

        }

        if (this.rbtnförnamnFörst.isSelected()) {
            skrivUtFörnamn();
        } else {
            skrivUtEfternamn();

        }


    }//GEN-LAST:event_BtnLäggtillActionPerformed
/**
     * Hanterar logiken för att radera en kontakt baserat på ID.
     */
    // Hämta ID från textfältet och ta bort mellanslag
    // Omvandla text till siffra
    // Sedan anropa DBManager för att utföra raderingen i databasen
    // Uppdatera listan på skärmen
    private void btnraderaKontaktActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnraderaKontaktActionPerformed
        // TODO add your handling code here:private void btnraderaKontaktActionPerformed(java.awt.event.ActionEvent evt) {                                                 
        try {

            String input = jTextField1.getText().trim();
            if (!input.isEmpty()) {
                int id = Integer.parseInt(input);
                db.delete(id);
                jTextField1.setText("");

                if (this.rbtnförnamnFörst.isSelected()) {
                    skrivUtFörnamn();
                } else {
                    skrivUtEfternamn();
                }
                System.out.println("Kontakt med ID " + id + " raderades.");
            } else {
                System.out.println("Du måste skriva ett ID i rutan!");
            }
        } catch (NumberFormatException e) {
            System.out.println("Fel: Du måste skriva en siffra (ID).");
        } catch (Exception e) {

        }
    }//GEN-LAST:event_btnraderaKontaktActionPerformed

