/**
     * Hämtar all data och skriver ut den med Förnamn först.
     */
    // Rensa rutan först
    // Hämtar kolumner från databasen och lägger till i textrutan
    private void skrivUtFörnamn() {
        this.txaLista.setText("");
        ResultSet rs = db.getAllData();
        if (rs != null) {
            try {
                while (rs.next()) {
                    this.txaLista.append(rs.getInt("kontaktid") + "\t "
                            + rs.getString("firstname") + "\t "
                            + rs.getString("lastname") + "\t "
                            + rs.getString("phonenumber") + "\n ");

                }
            } catch (SQLException ex) {
                System.out.println("Ingen data");
            }
        } else {
            System.out.println("Ingen data från databasen, kontrollera anslutningen.");
        }

    }

   /**
     * Hämtar all data och skriver ut den med Efternamn först.
     */
    // Rensa rutan först
